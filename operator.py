# Arithmetic Operators
a = 10
b = 3

print(a + b)   # Addition
print(a - b)   # Subtraction
print(a * b)   # Multiplication
print(a / b)   # Division
print(a % b)   # Modulus (remainder)
print(a ** b)  # Power
print(a // b)  # Floor division


# Comparison Operators
age = 20

print(age == 20)  # Equal
print(age != 18)  # Not equal
print(age > 18)   # Greater than
print(age < 25)   # Less than
print(age >= 18)  # Greater/equal
print(age <= 25)  # Less/equal


# Logical Operators
has_id = True
has_ticket = True

print(has_id and has_ticket)
print(has_id or has_ticket)
print(not has_id)


# Assignment Operators
salary = 5000

salary += 1000
print(salary)

salary -= 500
print(salary)